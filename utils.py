import os

############################################################
# CREATE DIRECTORY
############################################################
def mkdir(dirName):
    if (os.path.exists(dirName) == False):
        os.makedirs(dirName)
############################################################

############################################################
# CONVERT RGB IMAGE TO GRAYSCALE
############################################################
def RGBtoGray(img):
    gImg = (img[:,:,0]*0.30 + img[:,:,1]*0.59 + img[:,:,2]*0.11).astype('uint8')
    return gImg
